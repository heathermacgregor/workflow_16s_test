def load_qiime2_references(ref_dir: Path) -> Tuple[Artifact, Artifact]:
    """
    Loads the Greengenes2 reference phylogeny and taxonomy artifacts.
    
    Args:
        ref_dir: Directory containing the reference .qza files.

    Returns:
        A tuple containing the phylogeny and taxonomy artifacts.
    """
    gg2_phylogeny_path = ref_dir / "gg-2022-10-ssu-nr99-full-length-phylogeny.qza"
    gg2_taxonomy_path = ref_dir / "gg-2022-10-ssu-nr99-taxonomy.qza"

    if not gg2_phylogeny_path.exists() or not gg2_taxonomy_path.exists():
        logger.error(f"Greengenes2 reference files not found in '{ref_dir}'.")
        logger.info("Please download them from https://data.qiime2.org/2022.10/common/")
        sys.exit(1)

    logger.info("Loading Greengenes2 reference artifacts...")
    phylogeny = Artifact.load(str(gg2_phylogeny_path))
    taxonomy = Artifact.load(str(gg2_taxonomy_path))
    return phylogeny, taxonomy

def anndata_to_qiime2_artifacts(adata: ad.AnnData) -> Tuple[Artifact, Artifact]:
    """
    Converts data from an AnnData object into QIIME 2 Artifacts.

    Args:
        adata: The AnnData object to convert.

    Returns:
        A tuple of (FeatureTable[Frequency], FeatureData[Sequence]) artifacts.
    """
    subset_id = adata.uns.get('subset_id', 'unknown_subset')
    logger.info(f"Converting AnnData object '{subset_id}' to QIIME 2 artifacts.")

    # 1. Create BIOM table from adata.X (counts)
    # AnnData is (samples x features), BIOM/QIIME2 is (features x samples), so we transpose.
    X = adata.X
    # Convert to dense numpy array if necessary
    import numpy as np
    try:
        from scipy import sparse
        is_sparse = sparse.issparse(X)
    except ImportError:
        is_sparse = False

    if is_sparse:
        X_dense = X.A if hasattr(X, "A") else X.todense()
    else:
        # Handles numpy arrays, masked arrays, h5py arrays, etc.
        X_dense = np.asarray(X)
    counts_df = pd.DataFrame(X_dense.T, index=adata.var_names, columns=adata.obs_names)
    biom_table = biom.Table(counts_df.values, observation_ids=counts_df.index, sample_ids=counts_df.columns)
    table_artifact = Artifact.import_data("FeatureTable[Frequency]", biom_table)

    # 2. Create sequences series from adata.var
    # We assume representative sequences are stored in .var['sequence'] or .uns['rep_seqs']
    if 'sequence' in adata.var.columns:
        sequences_series = adata.var['sequence']
    elif 'rep_seqs' in adata.uns and isinstance(adata.uns['rep_seqs'], dict):
        # Ensure the sequence series is aligned with the feature table columns (adata.var_names)
        sequences_series = pd.Series(adata.uns['rep_seqs']).reindex(adata.var_names)
    else:
        raise ValueError(f"Representative sequences not found in AnnData object for '{subset_id}'. "
                         "Expected in `adata.var['sequence']` or `adata.uns['rep_seqs']`.")
    
    # Drop features with no sequence data
    sequences_series.dropna(inplace=True)
    
    seq_artifact = Artifact.import_data("FeatureData[Sequence]", sequences_series)

    return table_artifact, seq_artifact

def run_meta_analysis(project_dir: Path, ref_dir: Path):
    """
    Main function to run the phylogenetic meta-analysis pipeline.
    
    Args:
        project_dir: The root directory of the project.
        ref_dir: Directory containing QIIME 2 reference artifacts.
    """
    processed_data_dir = project_dir / "processed_data"
    output_dir = project_dir / "downstream_analysis"
    output_dir.mkdir(exist_ok=True)
    
    h5ad_files = list(processed_data_dir.glob("*.h5ad"))
    if not h5ad_files:
        logger.error(f"No .h5ad files found in '{processed_data_dir}'. Halting analysis.")
        return

    logger.info(f"Found {len(h5ad_files)} AnnData files to process.")
    
    gg2_phylogeny, gg2_taxonomy = load_qiime2_references(ref_dir)

    collapsed_tables = []
    all_metadata_dfs = []

    for h5ad_path in h5ad_files:
        try:
            logger.info(f"--- Processing: {h5ad_path.name} ---")
            adata = ad.read_h5ad(h5ad_path)
            
            # Skip if anndata is empty
            if adata.n_obs == 0 or adata.n_vars == 0:
                logger.warning(f"Skipping empty AnnData object: {h5ad_path.name}")
                continue

            # Step 1: Convert AnnData to QIIME 2 Artifacts
            table_qza, rep_seqs_qza = anndata_to_qiime2_artifacts(adata)

            # Step 2: Phylogenetic Placement with SEPP
            logger.info(f"Running phylogenetic placement (SEPP) for {adata.uns['subset_id']}...")
            placement_results = fragment_insertion.sepp(
                representative_sequences=rep_seqs_qza,
                reference_database=gg2_phylogeny,
                threads=4  # Adjust as needed
            )

            # Step 3: Collapse Feature Table to Species Level (Level 7)
            logger.info(f"Collapsing feature table for {adata.uns['subset_id']}...")
            collapsed_table_result = taxa_actions.collapse(
                table=table_qza,
                taxonomy=gg2_taxonomy,
                level=7  # Species level
            )
            collapsed_tables.append(collapsed_table_result.collapsed_table)

            # Collect metadata
            all_metadata_dfs.append(adata.obs)

        except Exception as e:
            logger.error(f"Failed to process {h5ad_path.name}. Error: {e}", exc_info=True)
            
    if not collapsed_tables:
        logger.error("No tables were successfully processed. Cannot proceed with merging.")
        return

    # Step 4: Merge Collapsed Feature Tables
    logger.info(f"Merging {len(collapsed_tables)} processed feature tables...")
    merged_table_result = feature_table_actions.merge(
        tables=collapsed_tables,
        overlap_method='sum'
    )
    merged_table_qza = merged_table_result.merged_table

    # Step 5: Combine Metadata
    logger.info("Combining metadata from all datasets...")
    combined_metadata_df = pd.concat(all_metadata_dfs, axis=0, join='outer', sort=False)
    # Ensure sample IDs are unique
    if combined_metadata_df.index.duplicated().any():
        logger.warning("Duplicate sample IDs found in metadata. Consider namespacing them if they originate from different datasets but share IDs.")

    # Step 6: Create Final Merged AnnData Object
    logger.info("Creating final merged AnnData object...")
    merged_counts_df = merged_table_qza.view(pd.DataFrame) # This is features x samples
    
    # Align metadata and feature table
    final_metadata = combined_metadata_df.reindex(merged_counts_df.columns)
    
    # Create the final AnnData object (transpose counts to be samples x features)
    final_adata = ad.AnnData(X=merged_counts_df.T, obs=final_metadata)
    final_adata.uns['phylogenetic_reference'] = 'Greengenes2 2022.10'
    
    # Save the final object
    final_output_path = output_dir / "meta_analysis_merged.h5ad"
    logger.info(f"Saving final merged AnnData object to: {final_output_path}")
    final_adata.write(final_output_path, compression="gzip")
    
    logger.info("--- Meta-analysis complete! ---")

