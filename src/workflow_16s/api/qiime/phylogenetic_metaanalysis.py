"""
Phylogenetic Meta-Analysis of 16S rRNA AnnData Objects
----------------------------------------------------------------------------------------
This script combines multiple AnnData objects (.h5ad files) generated by the
upstream pipeline. It leverages QIIME 2's fragment insertion functionality
to phylogenetically harmonize feature tables from different 16S rRNA subfragments,
allowing for a unified meta-analysis.

Execution requires a QIIME 2 conda environment.

Command-Line Usage:
    conda activate qiime2-shotgun-2024.5  # Or your specific QIIME 2 env
    python -m workflow_16s.downstream.meta_analysis --project_dir /path/to/your/project
"""

